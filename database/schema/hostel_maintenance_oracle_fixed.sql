CREATE TABLE hostels (
  id          VARCHAR2(36)  DEFAULT SYS_GUID() PRIMARY KEY,
  name        VARCHAR2(100) NOT NULL,
  description VARCHAR2(500),
  created_at  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT uq_hostel_name UNIQUE (name)
);

COMMENT ON TABLE  hostels       IS 'Campus hostel blocks';
COMMENT ON COLUMN hostels.id    IS 'UUID generated by SYS_GUID()';


CREATE TABLE rooms (
  id          VARCHAR2(36)  DEFAULT SYS_GUID() PRIMARY KEY,
  hostel_id   VARCHAR2(36)  NOT NULL,
  room_number VARCHAR2(20)  NOT NULL,
  floor_num   NUMBER(2),
  capacity    NUMBER(2)     DEFAULT 2,
  created_at  TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  CONSTRAINT fk_rooms_hostel    FOREIGN KEY (hostel_id) REFERENCES hostels(id) ON DELETE CASCADE,
  CONSTRAINT uq_room_in_hostel  UNIQUE (hostel_id, room_number)
);

CREATE INDEX idx_rooms_hostel ON rooms(hostel_id);


CREATE TABLE users (
  id              VARCHAR2(36)  DEFAULT SYS_GUID() PRIMARY KEY,
  role            VARCHAR2(10)  NOT NULL,
  reg_number      VARCHAR2(30)  UNIQUE,
  full_name       VARCHAR2(150) NOT NULL,
  email           VARCHAR2(150) NOT NULL,
  phone           VARCHAR2(20),
  password_hash   VARCHAR2(255) NOT NULL,
  avatar_url      VARCHAR2(500),

  -- Student fields
  programme       VARCHAR2(100),
  year_of_study   NUMBER(1),
  room_id         VARCHAR2(36),

  -- Worker fields
  specialisation  VARCHAR2(100),
  is_available    NUMBER(1)     DEFAULT 1,   -- 1=TRUE 0=FALSE

  is_active       NUMBER(1)     DEFAULT 1    NOT NULL,
  last_login_at   TIMESTAMP,
  created_at      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at      TIMESTAMP     DEFAULT SYSTIMESTAMP NOT NULL,

  -- Constraints
  CONSTRAINT chk_user_role
    CHECK (role IN ('student','worker','admin')),
  CONSTRAINT uq_user_email
    UNIQUE (email),
  CONSTRAINT chk_reg_number_format
    CHECK (
      reg_number IS NULL OR
      REGEXP_LIKE(reg_number, '^[a-z]+(/[a-z]+)?/[0-9]{2}/[0-9]{2}$')
    ),
  CONSTRAINT chk_is_available CHECK (is_available IN (0,1)),
  CONSTRAINT chk_is_active    CHECK (is_active    IN (0,1)),
  CONSTRAINT fk_users_room    FOREIGN KEY (room_id) REFERENCES rooms(id) ON DELETE SET NULL
);

