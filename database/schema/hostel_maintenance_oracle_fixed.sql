-- Dashboard stats
CREATE OR REPLACE VIEW v_dashboard_stats AS
SELECT
  COUNT(*)                                                        AS total_requests,
  COUNT(CASE WHEN status = 'pending'     THEN 1 END)             AS pending,
  COUNT(CASE WHEN status = 'in_progress' THEN 1 END)             AS in_progress,
  COUNT(CASE WHEN status = 'resolved'
    AND created_at >= TRUNC(SYSTIMESTAMP,'MM') THEN 1 END)       AS resolved_this_month,
  COUNT(CASE WHEN urgency = 'emergency'
    AND status NOT IN ('resolved','cancelled') THEN 1 END)       AS active_emergencies,
  ROUND(AVG(
    CASE WHEN resolved_at IS NOT NULL
    THEN (CAST(resolved_at AS DATE) - CAST(created_at AS DATE)) * 1440
    END
  ))                                                              AS avg_response_time_minutes
FROM issues;

-- Issue queue ordered by urgency
CREATE OR REPLACE VIEW v_issue_queue AS
SELECT
  i.id,
  i.ticket_number,
  i.title,
  i.category,
  i.urgency,
  i.status,
  i.created_at,
  i.deadline,
  r.room_number,
  h.name       AS hostel_name,
  u.full_name  AS submitted_by_name,
  u.reg_number AS submitted_by_reg,
  w.full_name  AS assigned_to_name,
  (SELECT COUNT(*) FROM issue_photos p WHERE p.issue_id = i.id) AS photo_count
FROM issues i
JOIN rooms   r ON r.id = i.room_id
JOIN hostels h ON h.id = r.hostel_id
JOIN users   u ON u.id = i.submitted_by
LEFT JOIN users w ON w.id = i.assigned_to
ORDER BY
  CASE i.urgency
    WHEN 'emergency' THEN 1
    WHEN 'high'      THEN 2
    WHEN 'medium'    THEN 3
    ELSE 4
  END,
  i.created_at ASC;

