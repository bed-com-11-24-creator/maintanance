--  TABLE: ISSUES  (maintenance requests)
CREATE TABLE issues (
  id               VARCHAR2(36)   DEFAULT SYS_GUID() PRIMARY KEY,
  ticket_number    VARCHAR2(20)   NOT NULL UNIQUE,   -- MNT-2024-0001
  submitted_by     VARCHAR2(36)   NOT NULL,
  room_id          VARCHAR2(36)   NOT NULL,
  assigned_to      VARCHAR2(36),
  escalated_to     VARCHAR2(36),

  title            VARCHAR2(200)  NOT NULL,
  description      CLOB,
  category         VARCHAR2(20)   NOT NULL,
  urgency          VARCHAR2(15)   DEFAULT 'medium'  NOT NULL,
  status           VARCHAR2(15)   DEFAULT 'pending' NOT NULL,

  escalated_at     TIMESTAMP,
  escalation_note  VARCHAR2(500),
  deadline         TIMESTAMP,
  resolved_at      TIMESTAMP,

  created_at       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,
  updated_at       TIMESTAMP      DEFAULT SYSTIMESTAMP NOT NULL,

  -- Constraints
  CONSTRAINT chk_issue_category
    CHECK (category IN ('electrical','plumbing','furniture','bathroom','internet','security','other')),
  CONSTRAINT chk_issue_urgency
    CHECK (urgency IN ('low','medium','high','emergency')),
  CONSTRAINT chk_issue_status
    CHECK (status IN ('pending','in_progress','resolved','escalated','cancelled')),
  CONSTRAINT fk_issue_submitted  FOREIGN KEY (submitted_by)  REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT fk_issue_room       FOREIGN KEY (room_id)       REFERENCES rooms(id),
  CONSTRAINT fk_issue_assigned   FOREIGN KEY (assigned_to)   REFERENCES users(id) ON DELETE SET NULL,
  CONSTRAINT fk_issue_escalated  FOREIGN KEY (escalated_to)  REFERENCES users(id) ON DELETE SET NULL
);

CREATE INDEX idx_issues_submitted  ON issues(submitted_by);
CREATE INDEX idx_issues_assigned   ON issues(assigned_to);
CREATE INDEX idx_issues_status     ON issues(status);
CREATE INDEX idx_issues_urgency    ON issues(urgency);
CREATE INDEX idx_issues_room       ON issues(room_id);
CREATE INDEX idx_issues_created    ON issues(created_at DESC);


