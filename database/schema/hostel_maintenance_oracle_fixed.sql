CREATE OR REPLACE TRIGGER trg_issue_status_change
AFTER UPDATE OF status ON issues
FOR EACH ROW
WHEN (OLD.status != NEW.status)
BEGIN
  INSERT INTO issue_timeline (id, issue_id, actor_id, event_type, note)
  VALUES (
    SYS_GUID(),
    :NEW.id,
    :NEW.assigned_to,
    'status_changed',
    'Status changed from ' || :OLD.status || ' to ' || :NEW.status
  );
END;
